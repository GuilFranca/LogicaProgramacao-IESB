// ceil (teto)
// Math.ceil(x)
// Math.ceil(8.74) = 9
// Math.ceil(x) = Maior número inteiro menor do que ou igual a x.

// Floor (chão)
// Math.floor(x)
// Math.floor(8.74) = 8
// Math.floor(x) = Menor número inteiro maior do que ou igual a x.

console.log(Math.ceil(5.5));
console.log(Math.floor(5.5));

// Power (potencia)
// 2**4 = 16
// Math.power(2,4)

console.log(Math.pow(2,4));
console.log(Math.pow(3,3));
console.log(Math.pow(-3,3));

// Round (arredondar)
// Math.round(2.8) = 3

console.log(Math.round(3.1));
console.log(Math.round(3.8));
console.log(Math.round(-3.8));

